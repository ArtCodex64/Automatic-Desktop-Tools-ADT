function install_bat(){
  userName="$(logname)"
  echo -en "${cyanColour}[*]${endColour} ${grayColour}Instalando bat ...${endColour}"
  cp ./tools/bat_0.18.2_amd64.deb /home/$userName/Descargas/ > /dev/null 2>&1
  (cd /home/$userName/Descargas ; dpkg -i bat_0.18.2_amd64.deb > /dev/null 2>&1)
  echo -e "${greenColour}(OK)${endColour}"
}
